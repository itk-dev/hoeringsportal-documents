namespace App\Util;

class TemplateHelper
{
    /** @var \Twig_Environment */
    private $twig;

    public function __construct(\Twig_Environment $twig)
    {
        $this->twig = $twig;
    }

    public function render(string $template, array $data)
    {
        return $this->twig->createTemplate($template)->render($data);
    }
}
